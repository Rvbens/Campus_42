#include "push_swap.h"

void	ft_solver(t_list *stk_a, int n, int n_bits)
{
	int	bit;
	int	i;

	bit = 0;
	while (bit < n_bits)
	{
		i = 0;
		while (i < n)
		{
			i++;
		}	
		bit++;
	}
	(void) stk_a;
}
