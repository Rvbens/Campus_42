int	ft_atoi(char *str)
{
	int	sig;
	int	out;

	while ((*str >= 9 && *str <= 13) || *str == ' ')
		str++;
	sig = 1;
	while (*str == '-' || *str == '+')
	{
		if (*str == '-')
			sig *= -1;
		str++;
	}
	out = 0;
	while (*str >= '0' && *str <= '9')
	{
		out = out * 10 + (*str - '0');
		str++;
	}
	return (sig * out);
}
