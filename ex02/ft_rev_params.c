#include <unistd.h>

int	main(int argn, char **argv)
{
	int	c;

	c = argn - 1;
	while (c)
	{
		while (*argv[c])
			write(1, argv[c]++, 1);
		write(1, &"\n", 1);
		c--;
	}
}
