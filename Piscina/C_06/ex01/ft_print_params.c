#include <unistd.h>

int	main(int argn, char **argv)
{
	int	c;

	c = 1;
	while (c < argn)
	{
		while (*argv[c])
			write(1, argv[c]++, 1);
		write(1, &"\n", 1);
		c++;
	}
}
